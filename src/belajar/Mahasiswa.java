public class Mahasiswa {
    String nama;
    String id;
    Boolean isKelas;

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public Boolean getIsKelas() {
        return isKelas;
    }

    public void setIsKelas(Boolean isKelas) {
        this.isKelas = isKelas;
    }
    
}
